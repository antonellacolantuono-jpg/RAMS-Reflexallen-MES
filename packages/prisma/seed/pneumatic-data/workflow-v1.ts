export async function seedWorkflowV1(
  prisma: Prisma,
  ctx: PneumaticSeedContext,
): Promise<{ workflowId: string; workflowVersionId: string }> {
  const item = ctx.items['PNE-TUBE-12-680']
  if (!item) throw new Error('Workflow v1 requires item PNE-TUBE-12-680 (must run D1 first)')

  // 1. Workflow
  const workflow = await prisma.workflow.upsert({
    where: { plantId_code: { plantId: ctx.plantId, code: PNE_WORKFLOW_V1_CODE } },
    update: { name: PNE_WORKFLOW_V1_NAME, itemId: item.id, updatedBy: SYSTEM },
    create: {
      code: PNE_WORKFLOW_V1_CODE,
      name: PNE_WORKFLOW_V1_NAME,
      itemId: item.id,
      description: 'Workflow demo Pneumatic Air M12 680mm — 4 fasi / 6 gruppi / 34 step. Cliente: Iveco/Volvo.',
      plantId: ctx.plantId,
      createdBy: SYSTEM,
      updatedBy: SYSTEM,
    },
  })

  // 2. WorkflowVersion v1 (status approved → PROMPT "Active")
  let version = await prisma.workflowVersion.findUnique({
    where: { workflowId_version: { workflowId: workflow.id, version: 1 } },
  })
  if (!version) {
    version = await prisma.workflowVersion.create({
      data: {
        workflowId: workflow.id,
        version: 1,
        status: 'approved',
        approvedBy: SYSTEM,
        approvedAt: new Date(),
        notes: 'Demo workflow seeded by PROMPT_PNE_2 D3.',
        createdBy: SYSTEM,
        updatedBy: SYSTEM,
      },
    })
  } else if (version.status !== 'approved') {
    version = await prisma.workflowVersion.update({
      where: { id: version.id },
      data: { status: 'approved', approvedBy: SYSTEM, approvedAt: new Date(), updatedBy: SYSTEM },
    })
  }
  if (workflow.currentVersionId !== version.id) {
    await prisma.workflow.update({
      where: { id: workflow.id },
      data: { currentVersionId: version.id, updatedBy: SYSTEM },
    })
  }

  // 3. Phases / Groups / Steps (idempotent by order within parent)
  for (const phaseDef of PNE_WORKFLOW_V1.phases) {
    let phase = await prisma.phase.findFirst({
      where: { workflowVersionId: version.id, order: phaseDef.order },
    })
    if (!phase) {
      phase = await prisma.phase.create({
        data: {
          workflowVersionId: version.id,
          order: phaseDef.order,
          category: phaseDef.category,
          name: phaseDef.name,
          isCycleBased: phaseDef.isCycleBased,
          createdBy: SYSTEM,
          updatedBy: SYSTEM,
        },
      })
    } else if (phase.name !== phaseDef.name || phase.category !== phaseDef.category || phase.isCycleBased !== phaseDef.isCycleBased) {
      await prisma.phase.update({
        where: { id: phase.id },
        data: { name: phaseDef.name, category: phaseDef.category, isCycleBased: phaseDef.isCycleBased, updatedBy: SYSTEM },
      })
    }

    for (const groupDef of phaseDef.groups) {
      let group = await prisma.group.findFirst({
        where: { phaseId: phase.id, order: groupDef.order },
      })
      if (!group) {
        group = await prisma.group.create({
          data: {
            phaseId: phase.id,
            order: groupDef.order,
            category: groupDef.category,
            name: groupDef.name,
            supportsParallel: groupDef.supportsParallel ?? false,
            supportsRecovery: groupDef.supportsRecovery ?? false,
            isAutoGenerated: groupDef.isAutoGenerated ?? false,
            description: groupDef.description ?? null,
            createdBy: SYSTEM,
            updatedBy: SYSTEM,
          },
        })
      } else {
        await prisma.group.update({
          where: { id: group.id },
          data: {
            name: groupDef.name,
            category: groupDef.category,
            supportsParallel: groupDef.supportsParallel ?? false,
            supportsRecovery: groupDef.supportsRecovery ?? false,
            isAutoGenerated: groupDef.isAutoGenerated ?? false,
            description: groupDef.description ?? null,
            updatedBy: SYSTEM,
          },
        })
      }

      for (const stepDef of groupDef.steps) {
        const skillId = stepDef.skillCode ? ctx.skills[stepDef.skillCode]?.id ?? null : null
        const deviceId = stepDef.deviceCode ? ctx.devices[stepDef.deviceCode]?.id ?? null : null
        const recipeId = stepDef.recipeCode ? ctx.recipes[stepDef.recipeCode]?.id ?? null : null

        const stepData = {
          order: stepDef.order,
          category: stepDef.category,
          actionType: stepDef.actionType,
          name: stepDef.name,
          instructions: stepDef.instructions ?? null,
          skillId,
          deviceId,
          recipeId,
          standardTimeSec: stepDef.standardTimeSec ?? null,
          isRequired: stepDef.isRequired ?? true,
          partReference: stepDef.partReference ?? null,
          deviceCategory: stepDef.deviceCategory ?? null,
        }

        const existing = await prisma.step.findFirst({
          where: { groupId: group.id, order: stepDef.order },
        })
        if (!existing) {
          await prisma.step.create({
            data: { ...stepData, groupId: group.id, createdBy: SYSTEM, updatedBy: SYSTEM },
          })
        } else {
          await prisma.step.update({
            where: { id: existing.id },
            data: { ...stepData, updatedBy: SYSTEM },
          })
        }
      }
    }
  }

  console.log(
    `✓ Workflow v1: ${PNE_WORKFLOW_V1_CODE} (${PNE_WORKFLOW_V1_COUNTS.phases} phases / ` +
      `${PNE_WORKFLOW_V1_COUNTS.groups} groups / ${PNE_WORKFLOW_V1_COUNTS.steps} steps; ` +
      `${PNE_WORKFLOW_V1_COUNTS.recoveryGroups} recovery-refs groups, hidden from linear flow)`,
  )
  return { workflowId: workflow.id, workflowVersionId: version.id }
}
