interface StepDef {
  order: number
  name: string
  category: string // StepCategory
  actionType: string // StepActionType
  deviceCategory?: 'pre' | 'device_main' | 'parallel' | 'post' | null
  partReference?: 'current' | 'previous' | 'next' | 'previous_n' | 'batch' | 'none' | null
  instructions?: string
  skillCode?: string // resolves to ctx.skills[code].id
  deviceCode?: string // resolves to ctx.devices[code].id
  recipeCode?: string // resolves to ctx.recipes[code].id
  standardTimeSec?: number
  isRequired?: boolean
}

interface GroupDef {
  order: number
  category: string // GroupCategory
  name: string
  supportsParallel?: boolean
  supportsRecovery?: boolean
  isAutoGenerated?: boolean
  description?: string
  steps: StepDef[]
}

interface PhaseDef {
  order: number
  category: string // PhaseCategory
  name: string
  isCycleBased: boolean
  groups: GroupDef[]
}

