import { describe, it, expect, vi, beforeEach } from 'vitest'
import { render, screen, waitFor } from '@testing-library/react'
import { QueryClient, QueryClientProvider } from '@tanstack/react-query'
import * as React from 'react'

vi.mock('next/navigation', () => ({ useRouter: () => ({ push: vi.fn() }) }))
vi.mock('next/link', () => ({ default: ({ children, href }: { children: React.ReactNode; href: string }) =>
  React.createElement('a', { href }, children) }))

const workOrdersGet = vi.fn()
const workOrdersAudit = vi.fn()
vi.mock('../../../../lib/sdk', () => ({
  sdk: {
    workOrders: {
      get: (...a: unknown[]) => workOrdersGet(...a),
      audit: (...a: unknown[]) => workOrdersAudit(...a),
    },
  },
}))

import WorkOrderDetailPage from './page'

function renderWithQuery(ui: React.ReactElement) {
  const client = new QueryClient({ defaultOptions: { queries: { retry: false, gcTime: 0 } } })
  return render(<QueryClientProvider client={client}>{ui}</QueryClientProvider>)
}

