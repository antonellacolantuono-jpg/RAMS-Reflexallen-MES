beforeEach(() => {
  workOrdersGet.mockReset()
  workOrdersAudit.mockReset()
})

describe('WorkOrderDetailPage', () => {
  it('renders 7 tabs with execution count badge from stepExecutionStats.total', async () => {
    workOrdersGet.mockResolvedValue(baseDetail)
    renderWithQuery(<WorkOrderDetailPage params={{ id: 'wo-1' }} />)

    await waitFor(() => {
      expect(screen.getAllByText('WO-2026-PNE-0042').length).toBeGreaterThan(0)
    })

    // 7 tabs present
    expect(screen.getByRole('tab', { name: /Panoramica/ })).toBeInTheDocument()
    expect(screen.getByRole('tab', { name: /Snapshot workflow/ })).toBeInTheDocument()
    expect(screen.getByRole('tab', { name: /Materiali/ })).toBeInTheDocument()
    // Execution tab includes the count badge (168)
    expect(screen.getByRole('tab', { name: /Esecuzione.*168/ })).toBeInTheDocument()
    expect(screen.getByRole('tab', { name: /Qualità/ })).toBeInTheDocument()
    expect(screen.getByRole('tab', { name: /Genealogia/ })).toBeInTheDocument()
    expect(screen.getByRole('tab', { name: /Attività/ })).toBeInTheDocument()
  })

  it('Panoramica tab shows KPI cards with target/produced/scrap/rework + assignment + phase progress', async () => {
    workOrdersGet.mockResolvedValue(baseDetail)
    renderWithQuery(<WorkOrderDetailPage params={{ id: 'wo-1' }} />)

    await waitFor(() => {
      expect(screen.getAllByText('WO-2026-PNE-0042').length).toBeGreaterThan(0)
    })

    // KPI cards
    expect(screen.getByText('Target')).toBeInTheDocument()
    expect(screen.getByText('Prodotti')).toBeInTheDocument()
    expect(screen.getByText('Scarto')).toBeInTheDocument()
    expect(screen.getByText('Rilavorati')).toBeInTheDocument()

    // Operator name from assignments
    expect(screen.getByText(/Mario Rossi · 1234/)).toBeInTheDocument()

    // Phase from snapshot rendered in phase progress card
    expect(screen.getByText('Avanzamento per fase')).toBeInTheDocument()
    expect(screen.getByText('Estrusione')).toBeInTheDocument()
  })

  it('Snapshot workflow tab renders phase + group + step from parsed snapshotData', async () => {
    workOrdersGet.mockResolvedValue(baseDetail)
    renderWithQuery(<WorkOrderDetailPage params={{ id: 'wo-1' }} />)

    await waitFor(() => {
      expect(screen.getAllByText('WO-2026-PNE-0042').length).toBeGreaterThan(0)
    })

    screen.getByRole('tab', { name: /Snapshot workflow/ }).click()

    await waitFor(() => {
      expect(screen.getByText(/Snapshot bloccato al rilascio/)).toBeInTheDocument()
    })
    // Phase header (Estrusione appears both in Overview phase-progress and here)
    expect(screen.getAllByText('Estrusione').length).toBeGreaterThan(0)
    expect(screen.getByText('Avvio linea')).toBeInTheDocument()
    expect(screen.getByText('Scan ricetta')).toBeInTheDocument()
  })

  it('Materiali tab renders BoM line components with required/consumed derived from progress', async () => {
    workOrdersGet.mockResolvedValue(baseDetail)
    renderWithQuery(<WorkOrderDetailPage params={{ id: 'wo-1' }} />)

    await waitFor(() => {
      expect(screen.getAllByText('WO-2026-PNE-0042').length).toBeGreaterThan(0)
    })

    screen.getByRole('tab', { name: /Materiali/ }).click()

    await waitFor(() => {
      expect(screen.getByText('Componente A')).toBeInTheDocument()
    })
    // Required = qty(2) × qtyTarget(100) = 200
    expect(screen.getByText('200')).toBeInTheDocument()
    // Consumed = qty(2) × qtyProduced(42) = 84
    expect(screen.getByText('84')).toBeInTheDocument()
  })

  it('shows error message when WO not found', async () => {
    workOrdersGet.mockRejectedValue(new Error('not found'))
    renderWithQuery(<WorkOrderDetailPage params={{ id: 'missing' }} />)

    await waitFor(() => {
      expect(screen.getByText(/Ordine di lavoro non trovato/)).toBeInTheDocument()
    })
  })
})
